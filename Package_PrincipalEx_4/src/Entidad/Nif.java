
package Entidad;
 

public class Nif {
 
    private double maxima;
    private double actual;

    public Nif() {
    }

    public Nif(double maxima, double actual) {
        this.maxima = maxima;
        this.actual = actual;
    }

    public double getMaxima() {
        return maxima;
    }

    public void setMaxima(double maxima) {
        this.maxima = maxima;
    }

    public double getActual() {
        return actual;
    }

    public void setActual(double actual) {
        this.actual = actual;
    }
    
    
    
    }
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
   

